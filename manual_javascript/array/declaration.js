// 리터럴들을 나열한 배열 정의
var a1 = [10,20,30,40,50]; // 크기 5인 배열 생성
var a2 = []; // 빈 배열 생성
var a3 = ["자료구조",84,{},null,undefined]; //크기가 5인 배열 생성

// 객체생성자 함수 Array()를 이용한 배열 정의
var b1 = new Array(10,20,30,40,50); // a1과 같음.
var b2 = new Array(); // a2와 같음
var b3 = new Array("자료구조",84,{},null,undefined); // a3 과 같음
var b4 = new Array(5); //크기가 5인 배열 생성함. 배열의 공간은 undefined로 채워짐.
